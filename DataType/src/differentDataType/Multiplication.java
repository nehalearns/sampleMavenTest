package differentDataType;

public class Multiplication {

	public void multiplicationTwoNumbers (int one, int two) {
		
		int sum = one * two;
		System.out.println(sum);
		
	}
	
	public void multiplicationThreeNumbers (int one, int two, int three) {
		
		int sum= one * two * three ;
		System.out.println(sum);
		
	}
	
	public void multiplicationFourNumbers (int one, int two, int three, int four) {
		
		int sum= one * two * three * four ;
		System.out.println(sum);
	}
}

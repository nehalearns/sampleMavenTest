package packageOne;

public class numberOperation {

	public static void main(String[] args) {
		
		int a = 20 ;
		int b = 30 ;
		
		int c= a + b ;
		
		System.out.println(c);
		
		
		
		
		a=c ;
		System.out.println(a);
		
		a= b+c ;
		System.out.println(a);
		
		int d = a+b+c ;
		System.out.println(d);
		
		
	}

}
